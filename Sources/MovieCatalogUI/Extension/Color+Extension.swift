import SwiftUI

public extension Color {
  
	static var colorGreyLight: Color {
    return Color("ColorGreyLight")
  }
  
	static var colorPrimary: Color {
    return Color("ColorPrimary")
  }
  
	static var colorSecondary: Color {
    return Color("ColorSecondary")
  }
  
	static var colorSecondaryLight: Color {
    return Color("ColorSecondaryLight")
  }
}
